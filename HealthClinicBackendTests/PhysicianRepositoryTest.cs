using HealthClinicBackend.Backend.Repository.DatabaseSql;
using Xunit;

namespace HealthClinicBackendTests
{
    public class PhysicianRepositoryTest
    {
        [Fact]
        public void TestGetAll_ReturnsNotNull()
        {
            // GIVEN
            var subject = new PhysicianDatabaseSql();

            // WHEN
            var result = subject.GetAll();

            // THEN
            foreach (var physician in result)
            {
                Assert.NotNull(physician.Address);
                Assert.NotNull(physician.Address.City);
                Assert.NotNull(physician.Specialization);
            }
            Assert.NotEmpty(result);
        }
    }
}